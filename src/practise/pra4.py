#### 题目：输出 9*9 乘法口诀表。

# 程序分析：分行与列考虑，共9行9列，i控制行，j控制列。
''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''''

def main():
    res = []
    
    for i in range(1, 10):
        for j in range(1, 10):
            res.append(i * j)
            
            if j == 9:
                print(res)
                res.clear()
                


if __name__ == "__main__":
    main()                